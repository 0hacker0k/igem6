            <p>
                &emsp;&emsp;Dr. Lance Chang is the Chief Executive Officer of <a href="https://www.twbioscience.com/" class="link_subtitle">Trade Wind Biotech Co., Ltd. (TWBIO)</a>. TWBIO is the first biotech company in Taiwan with a fundamental interest in synthetic biology. They have made a lot of effort in preserving precious natural resources by designing a probiotic cell factory.
            </p>
            <p>
                &emsp;&emsp;The interview with Dr. Chang was a problem-shooting discussion that focused on the unwelcome endotoxin. The outcome was very informative and constructive that enlightened us with not only a new development alternative, but also a realistic approach to its removal.
            </p>
            <p>
                &emsp;&emsp;During the meeting, Dr. Chang first informed us that the <i>E. coli</i> Nissle guts microbiome, which has been approved by FDA, could be our best alternative because it can constantly generate EPA in our intestines. Hence, we could shift our prototype from ingesting EPA passively to cultivating EPA-producing probiotics gut microbiota to absorb EPA actively. This insight was a brand new idea that largely reversed our idea of EPA ingestion.
            </p>
            <p>
                &emsp;&emsp;When it came to making our project more visible, Dr. Chang mentioned another fascinating option: vegetarian meat. Instead of packing the EPA into capsules, it would be very eye-catching if we extracted EPA from <i>E. coli</i> Nissle and incorporated it into vegan meat. This cutting-edge food innovation could not only make our product more competitive, but promote a new way of thinking about nutritional supplements.
            </p>
            <p>
                &emsp;&emsp;During our experiments, we were often hindered during transformation because the genes we wanted to transform were too big to deal with. Dr. Chang suggested that using a Fusion kit to clone our target gene would be more efficient and the success rate would also rise. At the same time, Dr. Chang also suggested we compare the efficiency of different promoters, which might smooth our process of producing EPA.
            </p>
