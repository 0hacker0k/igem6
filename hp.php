                &emsp;&emsp;Throughout the entire IHP journey, we put a lot of emphasis on the human practice evaluation. Reflecting on the impacted communities, we showed our responsiveness in the way of “relieving the shortage, providing the necessary” to be connected to the wider world. However, the process of refining doesn’t stop here, but should be considered a long-term goal.
            </p>
            <p>
                &emsp;&emsp;Therefore, to close the loop between our design and what is desired, we developed two values that should be followed thoroughly in the future work: (1) comprehensive perspectives on CVD prevention and (2) engaging with those impacted.
            </p>
