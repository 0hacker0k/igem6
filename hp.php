            <p>
                &emsp;&emsp;Mr. Wu Chih Kai is a local pharmacist who has been working in a local drugstore for years. Not only does he possess proficiency in pharmaceutics, but also is familiar with the local market situation.
            </p>
            <p>
                &emsp;&emsp;During the interview with Mr. Wu, he first reminded us to notice the drug interaction between EPA and other medications. For example, people taking anticoagulants cannot ingest EPA during their treatment due to the concern of synergism, causing the risk of massive hemorrhage. 
            </p>
            <p>
                &emsp;&emsp;When talking about the customers, Mr, Wu indicated that Taiwanese tend to overlook their health conditions. Therefore, a proper design of EPA supplement products that make it easy to use and store is necessary, and good propaganda through local media may be helpful for newly marketed products. Based on the suggestion from Mr. Wu, our plan is to sell future EPA supplement products in a quantity suitable for monthly consumption. The feedback from Mr. Wu provoked us to ponder the characteristics of our future product, including dosage, packaging, price, and any other information we would want to know when grabbing a bottle of nutrient supplement from the shelf in the pharmacy.
            </p>
