            <p>
                &emsp;&emsp;As mentioned in the overview, prevention of CVD is our priority. Therefore, we looked for a more comprehensive answer for CVD prevention by reaching out to a dietitian, Dr. Chen. She told us that prevention of CVDs should begin with the optimization of daily routines, particularly daily exercise and diets.
            </p>
            <p>
                &emsp;&emsp;Following the dietitian’s suggestion, our team came up with two ways to promote the two concepts of healthy diet and daily exercise: 
            </p>
            <p>
                &emsp;&emsp;We collaborated with worldwide iGEM teams to collect CVD-reducing recipes using local ingredients, and establish a <a href="https://2022.igem.wiki/ccu-taiwan/collaborations#9" class="link_subtitle">CVD-reducing cookbook</a> for people to get information on healthy eating more easily.
            </p>
            <p>
                &emsp;&emsp;At the same time, we collaborated with the Center for Innovative Research on Aging Society in our university to hold a <a href="https://2022.igem.wiki/ccu-taiwan/education#7" class="link_subtitle">campus walking activity</a> for hundreds of elderly living in nearby regions. In this activity, some participants shared their experience of fighting with CVDs, which greatly encouraged us. 
            </p>
            <div id="14" class="title-label-div">
                <h2 class="title_sub_label" style="background-image: url('<?php echo imgfile;?>/highlight.png')">We Can Make It Better!</h2>
            </div>
            <div class="full_block">
                <div class="img" style="--width:25%;">
                    <img src="<?php echo imgfile;?>/hp/ihp09.jpg"/>
                </div>
