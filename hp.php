            <div class="marge" style="--width:calc(3em + 5vh)"></div>
            <p>
                &emsp;&emsp;Dr. Yan-Chang Lee, currently a postdoctoral fellow in the Research Center for Precision Molding, National Chung Cheng University.
            </p>
            <p>
                &emsp;&emsp;In rural schools, it is impossible for teachers to have assistants to do preparation for laboratory activities. Therefore, we think that affordable, automatic, and remote lab equipment may encourage teachers in the rural schools to design more experimental courses to educate teenagers.
            </p>
            <p>
                &emsp;&emsp;Since bacterial culture and gel electrophoresis are essential experimental techniques in synthetic biology, we decided to start by producing an <a href="https://2022.igem.wiki/ccu-taiwan/hardware#3" class="link_subtitle">automatic gel preparation machine</a> and <a href="https://2022.igem.wiki/ccu-taiwan/hardware#2" class="link_subtitle">quantitative agar</a> faucet. Accordingly, we ask for feedback from Dr. Lee.
            </p>
            <p>
                &emsp;&emsp;Throughout the meeting, Dr. Lee emphasized the importance of knowing and meeting the user’s needs. He was interested in our design, pointing out that the potential clients for our quantitative agar faucet might probably be at institutions with limited learning resources. Being unable to afford fancy but costly utensils like electronic pipettes, our quantitative agar faucet could perfectly meet their needs, while electronic pipettes are too expensive to have.
            </p>
            <p>
                &emsp;&emsp;Following our idea of the automatic gel preparation machine, Dr. Lee has observed the trend of introducing automation into experimentation. He then gave us many insights into possible enhancements of the machine, including applying a microchannel to the heating device, the concept of injection molding, and recommending other quantitative devices for better preparation of solution. Incorporating these suggestions, the process of making agarose gels by our machine will be conducted more smoothly and efficiently.
            </p>
