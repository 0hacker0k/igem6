            <p>
                &emsp;&emsp;Dr. Chang-Jer Wu is currently a distinguished professor and the chairman of the Department of Food Science, National Taiwan Ocean University. He is also the secretary general of the Health Food Society of Taiwan.
            </p>
            <p>
                &emsp;&emsp;To translate the lab product into the market, we consulted with Dr. Wu. He reminded us that the endotoxin problem should be considered when using a prokaryotic system. Endotoxin is a kind of lipopolysaccharide (LPS), composing the outer cell wall of all gram-negative bacteria, which are responsible for the organization and stability of the outer membrane. Ingesting endotoxins will cause the symptoms of high fever, vasodilation, diarrhea, and in extreme cases, fatal shock. Therefore, in separating the desired product from the other cell components, great care must be taken to remove all the endotoxin.
            </p>
            <p>
                &emsp;&emsp;However, while endotoxin poses enormous threats to us, it would be our benefit if we could overcome this problem. Because if we make pure and high concentration EPA production a reality, we can shift our product from a nutritional supplement to a medical treatment, which generates far more profits.
            </p>
            <p>
                &emsp;&emsp;Dr. Wu also reminded us of the laws we would face in the future when selling. To protect consumers’ health, there are many restrictions we have to follow to fulfill the goal. While this warning isn't directly relevant to our current projects, we should always make hay while the sun shines.
            </p>
