            <div class="marge" style="--width:calc(3em + 5vh)"></div>
            <p>
                &emsp;&emsp;However, since our target audiences are mostly elderly, the design of the exercise plan should take physical strength and endurance into consideration. Dr. Shuk-Fong Lee is the chair of the department of athletic sports of our own campus, Chung Cheng University. She is an expert not only in exercise psychology, but also has done research on exercise plans for the elderly specifically. Dr. Lee’s expertise happens to coincide with our needs. Therefore, we consulted her with some questions about how CVD patients or potential patients should exercise and how we could promote daily exercise to the public.
            </p>
            <p>
                &emsp;&emsp;Dr. Lee reported that High Density Lipoprotein (HDL) increases in direct proportion with High Intensity Interval Training (HIIT), which is strenuous exercise for 30 seconds, followed by rest for 30 seconds, repeating the process for 3 minutes. In addition, she told us that the campus walking we held is effective but not efficient. It can achieve publicity about the importance of daily exercises, but isn’t the best choice to maintain health and prevent CVD. Instead, doing weight training, squats, and interval training with professional coaches will have a more significant effect.
            </p>
            <p>
                &emsp;&emsp;As a result, this interview gave us a whole new perspective on doing exercise. “When you see a person with great skin and fair body, you're going to ask if that person used body care products or took nutritional supplements. Few will ask, what kind of exercise have you done?”, said Professor Lee. While we seek external care to improve bodily health, it is crucial for us not to leave any muscle, piece of bone, or inch of skin behind. Professor Lee not only brought us new knowledge about daily exercise, but also roused our consciousness of health care, not based on taking supplements, but caring for ourselves.
            </p>
