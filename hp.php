            </p>
            <p>
                &emsp;&emsp;Keeping up with both negative and positive opinions should never be regarded as a transitional goal, but rather, a human practice that should be thoroughly implemented throughout all phases. Most importantly, we  always remind ourselves of the importance of the opportunity of sharing between both scientific and general communities.
